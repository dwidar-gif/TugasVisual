    protected final void datatable() {
        Object[] Baris = {"Hari/Tanggal","Kode Acara", "Nama Acara", "Anggaran"};
        tableModel = new DefaultTableModel(null, Baris);
        table_pengeluaran.setModel(tableModel);
        String sql = "select * from keluar";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                Date tanggal = hasil.getDate("tanggal");
                
                String kd_acara = hasil.getString("kd_acara");
                String nama_acara = hasil.getString("nama_acara");
                String anggaran = hasil.getString("anggaran");
                
                String[] data = {String.valueOf(tanggal),kd_acara,nama_acara,anggaran};
                tableModel.addRow(data);

            }
        } catch (Exception e) {
        }
    }

