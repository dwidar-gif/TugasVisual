    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
     String sql = "insert into keluar values (?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            java.sql.Date tglDate = new java.sql.Date(dctglkeluar.getDate().getTime());
            stat.setDate(1, tglDate);
            stat.setString(2, jkd.getText());
            stat.setString(3, jnm.getText());
            stat.setString(4, janggaran.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            jkd.requestFocus();
            datatable();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan " + e);
        } 
    }//GEN-LAST:event_btn_saveActionPerformed

